/* AWT(Abstract Window Toolkit)
 UI프로그램은 윈도우, 메뉴, 버튼, 라디오, 리스트 등 시각적인 컴포넌트들을 제공해서 상호자와 상호작용하게된다.
 자바는 UI 개발을 할 수 있도록 JDK에서 JFC(Java Foundation Classes)를 제공한다
 JFC = AWT + Swing

 AWT: 자바로 구현하지 않고, os에서 제공하는 컴포넌트를 그대로 사용한다.
 Swing: 자바가 직접 컴포넌트를 생성하여 AWT보다 CPU와 메모리를 많이 사용한다.

 FX는 SDK가 제공한다.

 */




